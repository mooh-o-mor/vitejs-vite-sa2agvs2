// Функция для нормализации местоположения
export function extractLocation(raw: string): string {
  if (!raw) return "";
  
  let result = raw;
  
  // 1. Заменяем по словарю (без учёта регистра)
  for (const [key, value] of Object.entries(locationMap)) {
    const regex = new RegExp(key, "gi");
    if (regex.test(result)) {
      result = result.replace(regex, value);
    }
  }
  
  // 2. Убираем "да" и "нет" в конце (опреснитель)
  result = result.replace(/\s+(да|нет)\s*$/i, "").trim();
  
  // 3. Убираем лишние пробелы
  result = result.replace(/\s+/g, " ").trim();
  
  // 4. Для координат (содержат цифры, градусы, минуты) — возвращаем как есть
  if (result.match(/[0-9°'-]/)) {
    return result;
  }
  
  // 5. Форматируем: первая буква каждого слова заглавная
  result = result.split(" ").map(word => {
    // Слова, которые должны быть в верхнем регистре (аббревиатуры)
    const upperWords = ["МСС", "КФ", "ФГУБ", "СРП", "КМН", "БЭП", "СЭП", "РП"];
    if (upperWords.includes(word.toUpperCase())) {
      return word.toUpperCase();
    }
    // Остальные — с заглавной буквы
    return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
  }).join(" ");
  
  // 6. Добавляем "п." перед названием порта, если это порт
  const isPort = /(Санкт-Петербург|Калининград|Владивосток|Мурманск|Астрахань|Корсаков|Кандалакша|Архангельск|Новороссийск|Севастополь|Керчь|Находка|Ванино|Холмск|Магадан|Анадырь|Певек|Дудинка|Тикси|Онега|Нарьян-Мар|Сабетта|Хатанга|Восточный|Темрюк|Беломорск|Усть-Луга|Светлый|Янтарный|Усть-Камчатск|Петропавловск-Камчатский|Витино|Пригородное|Волна)/.test(result);
  
  const hasPortPrefix = raw.toLowerCase().includes("п.");
  
  if (isPort && !hasPortPrefix && !result.startsWith("п.")) {
    result = `п. ${result}`;
  }
  
  // 7. Чистим запятые
  result = result.replace(/,\s*,/g, ",");
  
  return result;
}
