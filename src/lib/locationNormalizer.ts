/* ── Замены аббревиатур портов ── */

const PORT_REPLACE: [RegExp, string][] = [
  [/(^|[\s,])СПб\.?($|[\s,])/gi, "$1Санкт-Петербург$2"],
  [/(^|[\s,])Клнг\.?($|[\s,.])/gi, "$1Калининград$2"],
  [/(^|[\s,])КЛД\.?($|[\s,.])/gi, "$1Калининград$2"],
  [/(^|[\s,])Петр-Камчатский($|[\s,])/gi, "$1Петропавловск-Камчатский$2"],
  [/камыш[\s-]бурун/gi, "Камыш-Бурун"],
  [/керченский[\s-]+пр-?в\.?/gi, "Керченский пролив"],
];

/* ── Водные объекты и проливы ── */
const WATER_BODY = /^(керченский пролив|балтийское море|охотское море|берингово море|японское море|каспийское море|черное море|азовское море|опл порта кавказ|опл порта каказ)/i;

/* ── Слова с которых начинаются детали (перед ними ставим запятую) ── */
const DETAIL_START = /^(пр|причал|терминал|наб|набережная|гр|м\.|мыс|ул|якорная|плавпричал|ПД|СРП|СРЗ|КФ|МСС|КМРП|ССРЗ|КТПБ|КМН|Лукойл|р-н|з-д|завод|район)/i;

/* ── Аббревиатуры которые всегда в верхнем регистре ── */
const UPPER_ABBR = ["МСС","КНР","СЧКМ","СРП","СРЗ","КФ","КМРП","ССРЗ","КТПБ","КМН","БОФ","ФГБУ","ПД","СФ","СК"];

export function extractLocation(raw: string): string {
  if (!raw) return "";

  // 1. Убираем БЭП/СЭП в конце, потом Да/Нет
  let s = raw
    .replace(/\s*(БЭП|СЭП|CЭП)\s*$/i, "")
    .replace(/[\s,]+(да|нет)\s*$/i, "")
    .trim();

  // 2. Координаты не трогаем
  const isCoord =
    /\d{1,3}[-°\/]\d{1,2}[,.]?\d*\s*[NСнсCc°]/.test(s) ||
    /\d{1,3}\s+\d{1,2}[,.]?\d*\s*[NСнсNn]/.test(s) ||
    /\d{2,3}\s+\d{2}[,.]?\d*\s*(сев|в\.)/i.test(s);
  if (isCoord) return s;

  // 3. Убираем "пос." в начале
  s = s.replace(/^пос\.\s*/i, "");

  // 4. Убираем "п." в начале (включая "п. П.")
  s = s.replace(/^п\.\s+П\.\s*/i, "");
  s = s.replace(/^п\.\s*/i, "");

  // 5. Заменяем аббревиатуры портов
  for (const [re, replacement] of PORT_REPLACE) {
    s = s.replace(re, replacement);
  }

  // 6. Водные объекты — возвращаем без изменений
  if (WATER_BODY.test(s.trim())) {
    const parts = s.trim().split(/\s+/);
    const di = parts.findIndex((p, i) => i > 0 && /^(р-н|з-д|район|завод)/i.test(p));
    if (di > 0) return parts.slice(0, di).join(" ") + ", " + parts.slice(di).join(" ");
    return s.trim();
  }

  // 7. Убираем лишнее в конце, двойные пробелы и двойные запятые
  s = s
    .replace(/[.,\s]+$/, "")
    .trim()
    .replace(/\s{2,}/g, " ")
    .replace(/,\s*,+/g, ",");

  // 8. Расставляем запятую между портом и деталями
  const firstPart = s.split(/\s+\d/)[0];
  if (!firstPart.includes(",")) {
    const parts = s.split(/\s+/);
    const di = parts.findIndex((p, i) => i > 0 && DETAIL_START.test(p));
    if (di > 0) {
      s = parts.slice(0, di).join(" ") + ", " + parts.slice(di).join(" ");
    }
  }

  // 9. Восстанавливаем аббревиатуры в верхний регистр
  for (const abbr of UPPER_ABBR) {
    s = s.replace(new RegExp(`\\b${abbr}\\b`, "gi"), abbr);
  }

  return s;
}
